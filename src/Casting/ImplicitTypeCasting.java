package Casting;

public class ImplicitTypeCasting
{
 //////=======Implicit casting
//The process of converting one
//type of object and variable into another type is referred to as Typecasting. 
//When the conversion automatically performs by the compiler without the programmer's interference, 
//it is called implicit type casting or widening casting.
	public static void main(String[] args) 
	{
		int a=40;   //int 4 byte
		System.out.println("Implicit Casting/ Widening Casting");
		System.out.println("Value of A: "+a);
		
		
		long b=a;    //8 byte
		System.out.println("Value of A is  assign to B: "+b);
		
System.out.println();
		
		
		
	}

}
